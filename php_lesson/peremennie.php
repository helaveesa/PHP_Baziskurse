<?php
$a = 52;
// отрицание    
$a = -$a; // -52

echo $a;

// деление по модулю
$a = 52;
$b = 10;
$c = $a % $b; // 52 % 10 = 2
echo $c; // 2

// Префиксный инкремент
$c = ++$b;
echo 'c = ' . $c . ' , b = ' . $b;

// Постфиксный инкремент
$c = $b++;
echo 'c = ' . $c . ' , b = ' . $b;

// Префиксный декремент
$c = --$b;
echo 'c = ' . $c . ' , b = ' . $b; // c = 9, b = 9

// Постфиксный декремент
$c = $b--;
echo 'c = ' . $c . ' , b = ' . $b; // c = 10, b = 9

?>